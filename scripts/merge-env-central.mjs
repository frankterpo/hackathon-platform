#!/usr/bin/env node
/**
 * Merge all `.env.vercel.*` pulls into one `.env.local` with canonical names for this app.
 * Vercel often uses SUPABASE_PROJECT_URL, SUPABASE_ANON_PUBLIC_KEY, SUPABASE_SERVICE_ROLE_SECRET;
 * Next + this repo expect NEXT_PUBLIC_SUPABASE_URL, NEXT_PUBLIC_SUPABASE_ANON_KEY, SUPABASE_SERVICE_ROLE_KEY.
 *
 * Usage: node scripts/merge-env-central.mjs
 * Merge order matches scripts/sync-vercel-envs.sh (later files win on duplicate keys).
 *
 * Preserves keys from an existing `.env.local` that never appear in any `.env.vercel.*`
 * (e.g. ADMIN_SECRET) unless you set ENV_CENTRAL_NO_PRESERVE=1.
 */

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

import {
  encodeDotenvValue,
  parseDotenvLines,
} from "./lib/parse-dotenv-lines.mjs";

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");

/** Same order as scripts/sync-vercel-envs.sh — last entry wins conflicts. */
const VERCEL_ENV_BASE = [
  ".env.vercel.cursor-hackathon-london-2026-1",
  ".env.vercel.cursor-hackathon-london-2026-0",
  ".env.vercel.cursor-hack-london-2026-1",
  ".env.vercel.cursor-thrads-london-2026",
];

const discovered = fs
  .readdirSync(ROOT)
  .filter(
    /** @param {string} f */
    (f) =>
      f.startsWith(".env.vercel.") &&
      f !== ".env.vercel.all.merged" &&
      !VERCEL_ENV_BASE.includes(f),
  )
  .sort();

/** Later entries win on duplicate keys (same order idea as sync-vercel-envs.sh). */
const VERCEL_ENV_FILES = [...VERCEL_ENV_BASE, ...discovered];

/** Map Vercel / CLI names → canonical names for this repo. */
const TO_CANONICAL = {
  SUPABASE_PROJECT_URL: "NEXT_PUBLIC_SUPABASE_URL",
  SUPABASE_URL: "NEXT_PUBLIC_SUPABASE_URL",
  SUPABASE_ANON_PUBLIC_KEY: "NEXT_PUBLIC_SUPABASE_ANON_KEY",
  SUPABASE_SERVICE_ROLE_SECRET: "SUPABASE_SERVICE_ROLE_KEY",
};

const ALIAS_SOURCES = new Set(Object.keys(TO_CANONICAL));

function extractSupabaseRef(supabaseUrl) {
  const u = String(supabaseUrl).replace(/\/+$/, "");
  const m = u.match(/https?:\/\/([a-z0-9_-]+)\.supabase\.co$/i);
  return m?.[1] ?? null;
}

function readEnvFile(filePath) {
  if (!fs.existsSync(filePath)) {
    return [];
  }
  return parseDotenvLines(fs.readFileSync(filePath, "utf8"));
}

const raw = new Map();
const fromVercelRawKeys = new Set();

for (const rel of VERCEL_ENV_FILES) {
  const fp = path.join(ROOT, rel);
  const pairs = readEnvFile(fp);
  if (pairs.length === 0) {
    continue;
  }
  for (const [k, v] of pairs) {
    fromVercelRawKeys.add(k);
    raw.set(k, v);
  }
}

if (raw.size === 0) {
  console.error(
    "No data found. Run `npm run env:vercel` first (creates .env.vercel.<slug>), then re-run this script.",
  );
  process.exit(1);
}

const canonical = new Map();
for (const [k, v] of raw) {
  const target = TO_CANONICAL[k];
  if (target) {
    canonical.set(target, v);
  }
}

if (canonical.has("NEXT_PUBLIC_SUPABASE_URL")) {
  const ref = extractSupabaseRef(canonical.get("NEXT_PUBLIC_SUPABASE_URL"));
  if (ref) {
    canonical.set("NEXT_PUBLIC_SUPABASE_PROJECT_REF", ref);
  }
}

// Publishable key (new Supabase dashboard) — pass through under a stable public name
if (raw.has("SUPABASE_PUBLISHABLE_KEY")) {
  canonical.set(
    "NEXT_PUBLIC_SUPABASE_PUBLISHABLE_KEY",
    raw.get("SUPABASE_PUBLISHABLE_KEY"),
  );
}

const passthrough = new Map();
for (const [k, v] of raw) {
  if (ALIAS_SOURCES.has(k)) {
    continue;
  }
  if (k === "SUPABASE_PUBLISHABLE_KEY") {
    continue;
  }
  if ([...canonical.keys()].includes(k)) {
    continue;
  }
  passthrough.set(k, v);
}

const preservePath = path.join(ROOT, ".env.local");
const extras = new Map();
if (
  process.env.ENV_CENTRAL_NO_PRESERVE !== "1" &&
  fs.existsSync(preservePath)
) {
  const before = readEnvFile(preservePath);
  for (const [k, v] of before) {
    if (fromVercelRawKeys.has(k)) {
      continue;
    }
    if (ALIAS_SOURCES.has(k)) {
      continue;
    }
    if (canonical.has(k)) {
      continue;
    }
    extras.set(k, v);
  }
}

const allWrittenKeys = new Set([
  ...canonical.keys(),
  ...extras.keys(),
  ...passthrough.keys(),
]);

const header = [
  "# ═══════════════════════════════════════════════════════════════════",
  "# Central env — generated by: npm run env:central",
  "# Sources (last wins): " +
    VERCEL_ENV_FILES.filter((f) => fs.existsSync(path.join(ROOT, f))).join(
      ", ",
    ),
  "# Re-pull Vercel: npm run env:vercel   ·   Re-merge: npm run env:central",
  "# Do not commit this file (.gitignore). Rotate keys if they were ever pasted in chat.",
  "# ═══════════════════════════════════════════════════════════════════",
  "",
  "# --- Canonical (Next.js + hackathon-platform) ---",
];

const lines = [...header];
for (const k of [...canonical.keys()].sort()) {
  lines.push(`${k}=${encodeDotenvValue(canonical.get(k))}`);
}
if (extras.size > 0) {
  lines.push(
    "",
    "# --- Kept from previous .env.local (not in Vercel pulls) ---",
  );
  for (const k of [...extras.keys()].sort()) {
    lines.push(`${k}=${encodeDotenvValue(extras.get(k))}`);
  }
}
if (passthrough.size > 0) {
  lines.push("", "# --- From Vercel env pulls (shared / tooling) ---");
  for (const k of [...passthrough.keys()].sort()) {
    lines.push(`${k}=${encodeDotenvValue(passthrough.get(k))}`);
  }
}

const outPath = path.join(ROOT, ".env.local");
fs.writeFileSync(outPath, lines.join("\n") + "\n", "utf8");

console.log(
  `Wrote ${outPath} (${allWrittenKeys.size} keys). Restart: npm run dev`,
);
